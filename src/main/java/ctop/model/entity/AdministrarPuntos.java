package ctop.model.entity;

public class AdministrarPuntos {
	
	/**
	 * Clase muy sencilla para hacer más fácil la suma y resta de puntos
	 * 
	 *  @version 1.0
	 */
	private int puntos;
	private String numct;
	
	public String getNumct() {
		return numct;
	}

	public void setNumct(String numct) {
		this.numct = numct;
	}

	public int getPuntos() {
		return puntos;
	}

	public void setPuntos(int puntos) {
		this.puntos = puntos;
	}
	
	

}
